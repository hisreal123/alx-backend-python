#!/usr/bin/env python3
""" Description: type-annotated function add that takes a float a and a float b
                 as arguments and returns their sum as a float
    Arguments: a: float
                b: float
"""


def add(a: float, b: float) -> float:
    """Adds two float numbers and returns the result as a tuple."""
    return a + b  # the , at the end makes is a tuple.
